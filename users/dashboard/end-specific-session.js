const Screenshots = require('../../screenshots.js')

module.exports = async (device) => {
  await Screenshots.createUser(device, 'owner')
  const user = await Screenshots.createUser(device)
  user.sessionid = await user.sessionid()
  await user.hover('Account')
  await user.hover('My account')
  await user.screenshot('Click "My account"')
  await user.hover('Account')
  await user.click('My account')
  await user.hover('Sessions')
  await user.screenshot('Click "Sessions"')
  await user.hover('Sessions')
  await user.click('Sessions')
  await user.hover(user.sessionid, true)
  await user.screenshot('Click session to end')
  await user.hover(user.sessionid, true)
  await user.click(user.sessionid, true)
  await user.hover('End session')
  await user.screenshot('Click "End session"')
  await user.hover('End session')
  await user.click('End session')
  await user.hover('End session', true)
  await user.screenshot('Submit form')
  await user.hover('End session', true)
  await user.click('End session', true)
  await user.screenshot('Success')
}
